-- bots テーブルのインデックス (§11.2)
-- -----------------------------------------------------------------------------

-- ボット書き込み検索用 (§11.2)
-- GitHub Actions の bot-scheduler が書き込み対象ボットを選択する際に使用する。
-- is_active = true で活動中ボットを絞り込み、daily_id で当日の偽装IDを照合する。
CREATE INDEX idx_bots_is_active_daily_id
    ON bots (is_active, daily_id);
