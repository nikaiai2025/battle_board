-- -----------------------------------------------------------------------------
-- threads テーブルのインデックス (§11.2)
-- -----------------------------------------------------------------------------

-- スレッド一覧ソート用 (§11.2)
-- subject.txt のスレッド一覧生成クエリで主に使用する。
-- board_id で板を絞り込み、last_post_at 降順でアクティブなスレッドを上位表示する。
CREATE INDEX idx_threads_board_id_last_post_at
    ON threads (board_id, last_post_at DESC);

-- -----------------------------------------------------------------------------
-- posts テーブルのインデックス (§11.2)
-- -----------------------------------------------------------------------------

-- スレッド内レス取得用 (§11.2)
-- DAT フォーマット構築時・スレッド閲覧時に (thread_id, post_number) の順で取得する。
-- 注: 00001 の UNIQUE制約 posts_thread_id_post_number_unique が既にインデックスとして機能するが、
--     §11.2 の明示的な定義に従い記載（重複するため実際の適用はスキップ可能）。
-- (UNIQUE制約 posts_thread_id_post_number_unique のインデックスが既に存在するため省略)

-- Range差分応答用 (§11.2, §11.3)
-- 専ブラの Range: bytes=N- ヘッダに対応した差分レス取得に使用する。
-- thread_id で絞り込み、created_at で指定バイトオフセット以降のレスを検索する。
CREATE INDEX idx_posts_thread_id_created_at
    ON posts (thread_id, created_at);

-- 書き込み履歴用 (§11.2)
-- author_id でユーザーの書き込み一覧を取得し、created_at 降順でマイページに表示する。
CREATE INDEX idx_posts_author_id_created_at
    ON posts (author_id, created_at);

