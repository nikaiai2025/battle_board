/**
 * 配列からランダムに1要素を選択する純粋関数（数学的ランダム）。
 *
 * See: features/command_omikuji.feature @おみくじ結果は100件のセットからランダムに選択される
 *
 * @param items - 選択元の配列（空でないこと）
 * @returns ランダムに選ばれた要素
 */
function selectRandom<T>(items: readonly T[]): T {
	const index = Math.floor(Math.random() * items.length);
	return items[index];
}
